    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        // salvar
        try {
            if (jComboBox1.getSelectedItem() == null || jComboBox2.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Selecione o Rack e o Patch Panel.");
                return;
            }

            PortaTelefone telefone = new PortaTelefone();
            telefone.setTipo("Telefone");
            telefone.setCodigo(jTextField1.getText());
            telefone.setPorta(jTextField1.getText());
            telefone.setRamal(jTextField2.getText());
            telefone.setCorCabo(jTextField3.getText());
            telefone.setLocalizacao(jTextField4.getText());
            telefone.setUsuarioFinal(jTextField5.getText());
            telefone.setObservacao(jTextField6.getText());
            telefone.setOcupada(jCheckBox1.isSelected());

            Rack rack = (Rack) jComboBox1.getSelectedItem();
            PatchPanel patch = (PatchPanel) jComboBox2.getSelectedItem();
            telefone.setRackId(rack.getId());
            telefone.setPatchPanelId(patch.getId());
            String portaDigitada = jTextField3.getText(); // a porta que o usuário digitou
            TelefoneDAO dao = new TelefoneDAO();
            if (dao.portaOcupada(portaDigitada, patch.getId())) {
                JOptionPane.showMessageDialog(this, "Esta porta já está ocupada neste patch panel.");
                return;
            }
            dao.inserir(telefone);
            JOptionPane.showMessageDialog(this, "Porta de telefone adicionada com sucesso!");
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar porta: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }


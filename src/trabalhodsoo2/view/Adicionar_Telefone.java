    private void carregarRacks() {
        RackDAO dao = new RackDAO();
        List<Rack> lista = dao.listar();
        jComboBox1.removeAllItems();
        for (Rack r : lista) {
            jComboBox1.addItem(r);
        }
    }

    private void atualizarPatchPanels() {
        Rack rack = (Rack) jComboBox1.getSelectedItem();
        if (rack == null) {
            jComboBox2.removeAllItems();
            return;
        }
        PatchPanelDAO dao = new PatchPanelDAO();
        List<PatchPanel> lista = dao.listarPorRackETipo(rack.getId(), "Telefone");
        jComboBox2.removeAllItems();
        for (PatchPanel p : lista) {
            jComboBox2.addItem(p);
        }
    }


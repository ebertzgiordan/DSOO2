    private void carregarTabela() {
        RackDAO dao = new RackDAO();
        List<Rack> lista = dao.listar();

        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setRowCount(0);

        for (Rack r : lista) {
            modelo.addRow(new Object[]{
                r.getId(),
                r.getNome(),
                r.getLocalizacao()
            });
        }
    }


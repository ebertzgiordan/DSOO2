    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Editar
        int row = tabela.getSelectedRow();
        if (row >= 0) {
            int id = (int) tabela.getValueAt(row, 0);
            String nome = (String) tabela.getValueAt(row, 1);
            String localizacao = (String) tabela.getValueAt(row, 2);

            String novoNome = JOptionPane.showInputDialog("Novo nome:", nome);
            String novaLocalizacao = JOptionPane.showInputDialog("Nova localização:", localizacao);

            if (novoNome != null && novaLocalizacao != null) {
                Rack rack = new Rack(novoNome, novaLocalizacao);
                rack.setId(id);

                RackDAO dao = new RackDAO();
                dao.atualizar(rack);
                carregarTabela();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um rack para editar.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Remover
        if (tabela.getSelectedRow() != -1) {
            int id = (int) tabela.getValueAt(tabela.getSelectedRow(), 0);

            int confirm = JOptionPane.showConfirmDialog(null,
                    "Deseja remover este Rack?", "Confirmação",
                    JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                new RackDAO().excluir(id);
                carregarTabela();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Rack para remover.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

